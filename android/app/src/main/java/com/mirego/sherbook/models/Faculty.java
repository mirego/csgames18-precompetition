package com.mirego.sherbook.models;


public class Faculty {

    private String nom;
    private int score;
    private String url_symbol;

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getUrl() {
        return url_symbol;
    }

    public void setUrl(String url_symbol) {
        this.url_symbol = url_symbol;
    }


}